#include<iostream>
using namespace std;
int main()
{
    //
    //  Variabile Contatore int
    //
    int count=0; 

    //
    //  Ciclo che incrementa la variabile 
    // 
    for(int x=0; x<10; x++)
    {
        count++;
    }
    //
    //  Visualizzazione Variabile 
    // 
    cout<<count<<endl;
    system("pause");
}
