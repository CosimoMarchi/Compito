#include<iostream>
using namespace std;
int main()
{
    //
    //  Variabile Contatore int
    //
    int conta=0; 

    //
    //  Ciclo che incrementa la variabile 
    // 
    do
    {
        conta++;
    }while(conta<10);

    //
    //  Stampa Variabile 
    // 
    cout<<conta<<endl;
    system("pause");
}
