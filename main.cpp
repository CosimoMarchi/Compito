#include<iostream>
using namespace std;
int main()
{
    //
    //  Variabile Contatore int
    //
    int conta=0; 

    //
    //  Ciclo che incrementa la variabile 
    // 
    for(int x=0; x<10; x++)
    {
        conta++;
    }
    //
    //  Stampa Variabile 
    // 
    cout<<conta<<endl;
    system("pause");
}
