#include<iostream>
using namespace std;
int main()
{
    //
    //  Variabile Contatore di tipo int
    //
    int conta=0; 

    //
    //  Ciclo che incrementa la variabile 
    // 
    for(int i=0; i<10; i++)
    {
        conta++;
    }
    //
    //  Stampa Variabile 
    // 
    cout<<conta<<endl;
    system("pause");
}
